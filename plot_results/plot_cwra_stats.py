def classify_compound(smi: str) -> Dict[str, bool]:
    if Chem is None or MurckoScaffold is None:
        raise RuntimeError("RDKit is required to compute reliable-negative structural features.")

    result = {
        "is_secosteroidal": False,
        "c2_modified": False,
        "pentacyclic": False,
        "has_o5ring": False,
        "sidechain_cycle": False,
    }
    mol = Chem.MolFromSmiles(str(smi))
    if mol is None:
        return result

    has_a_ring = any(pat and mol.HasSubstructMatch(pat) for pat in A_RING_PATTERNS)
    if has_a_ring:
        result["is_secosteroidal"] = True
    else:
        for pat in SECO_PATTERNS:
            if pat and mol.HasSubstructMatch(pat):
                oh_count = len(mol.GetSubstructMatches(Chem.MolFromSmarts("[OH]")))
                if oh_count >= 2:
                    result["is_secosteroidal"] = True
                    break

    result["c2_modified"] = any(pat and mol.HasSubstructMatch(pat) for pat in C2_PATTERNS)
    n_rings = mol.GetRingInfo().NumRings()
    result["pentacyclic"] = n_rings >= 5
    result["has_o5ring"] = bool(O5_PATTERN and mol.HasSubstructMatch(O5_PATTERN))

    try:
        core = MurckoScaffold.GetScaffoldForMol(mol)
        core_rings = core.GetRingInfo().NumRings()
        if n_rings > core_rings:
            result["sidechain_cycle"] = True
        elif n_rings >= 2:
            ring_sizes = [len(r) for r in mol.GetRingInfo().AtomRings()]
            if any(s <= 6 for s in ring_sizes):
                result["sidechain_cycle"] = True
    except Exception:
        pass

    return result


def compute_reliable_negative_stats(pu_labels_path: str, composed_csv_path: str) -> Dict:
    if not os.path.exists(pu_labels_path):
        raise FileNotFoundError(f"Reliable-negative labels file not found: {pu_labels_path}")
    if not os.path.exists(composed_csv_path):
        raise FileNotFoundError(f"Composed modalities file not found: {composed_csv_path}")

    labels = pd.read_csv(pu_labels_path)
    if "pu_label" not in labels.columns:
        raise ValueError("pu_labels.csv must contain a 'pu_label' column.")

    rn_labels = labels[labels["pu_label"] == 0].copy()
    if rn_labels.empty:
        raise ValueError("No reliable negatives found (pu_label==0).")

    comp = pd.read_csv(composed_csv_path)
    smiles_col = "smiles" if "smiles" in comp.columns else "SMILES" if "SMILES" in comp.columns else None
    if smiles_col is None:
        raise ValueError("Composed CSV must contain either 'smiles' or 'SMILES' column.")

    if "index" in rn_labels.columns:
        rn_idx = pd.to_numeric(rn_labels["index"], errors="coerce").dropna().astype(int)
        rn_idx = rn_idx[rn_idx.isin(comp.index)]
        rn_smiles = comp.loc[rn_idx, smiles_col].dropna()
    elif "id" in rn_labels.columns and "id" in comp.columns:
        rn_ids = rn_labels["id"].dropna()
        rn_smiles = comp[comp["id"].isin(rn_ids)][smiles_col].dropna()
    else:
        raise ValueError(
            "Could not map reliable negatives to composed CSV. Expected "
            "'index' in pu_labels.csv or shared 'id' column."
        )

    feats = rn_smiles.astype(str).apply(classify_compound)
    feat_df = pd.DataFrame(feats.tolist())
    feat_df["non_steroidal"] = ~feat_df["is_secosteroidal"]

    n = len(feat_df)
    cnt = {cat: int(feat_df[cat].sum()) for cat in CATS}
    pct = {cat: round((cnt[cat] / n) * 100.0, 1) if n > 0 else 0.0 for cat in CATS}
    return {"n": int(n), "cnt": cnt, "pct": pct}


