def draw_fisher_single_line(ax: plt.Axes, report: Dict) -> None:
    x_step = 0.12
    entries = []
    x_counter = 0
    for comp, comp_short, _ in FOREST_DEFS:
        rows = _ordered_fisher_rows(report, comp)
        for item in rows:
            entries.append(
                {
                    "comp": comp,
                    "comp_short": comp_short,
                    "cat": item["cat"],
                    "x": x_counter * x_step,
                    "y": _bounded_log10_odds(item),
                    "sig": str(item["sig"]),
                }
            )
            x_counter += 1

    if not entries:
        ax.text(0.5, 0.5, "No Fisher data available", ha="center", va="center")
        ax.axis("off")
        return

    ax.axhline(0, color="#333333", linewidth=0.95, linestyle="--", alpha=0.8, zorder=1)

    for e in entries:
        y = e["y"]
        sig = e["sig"]
        if sig == "ns":
            color = "#B7B7B7"
        else:
            color = "#4E79A7" if y >= 0 else "#E15759"
        ax.vlines(e["x"], 0, y, color=color, linewidth=8.0, alpha=0.95, zorder=3)
        ax.scatter(e["x"], y, s=32, color=color, edgecolor="white", linewidth=0.5, zorder=4)
        if sig != "ns":
            y_star = y + 0.14 if y >= 0 else y - 0.14
            va = "bottom" if y >= 0 else "top"
            ax.text(e["x"], y_star, sig, ha="center", va=va, fontsize=9.8, fontweight="bold")

    n_per_comp = len(CAT_LABELS)
    y_subtitle = 2.34
    for j, (_, comp_short, _) in enumerate(FOREST_DEFS):
        start = j * n_per_comp * x_step
        end = start + (n_per_comp - 1) * x_step
        mid = (start + end) / 2.0
        ax.text(
            mid,
            y_subtitle,
            comp_short,
            ha="center",
            va="bottom",
            fontsize=10.2,
            fontweight="medium",
        )
        if j > 0:
            ax.axvline(
                start - (x_step / 2.0),
                color="#999999",
                linewidth=0.7,
                alpha=0.6,
                zorder=2,
            )

    x_margin = max(0.04, x_step * 0.35)
    ax.set_xlim(-x_margin, (len(entries) - 1) * x_step + x_margin)
    ax.set_ylim(-2.35, 2.5)
    ax.set_yticks([-2, -1, 0, 1, 2])
    ax.set_yticklabels(["0.01", "0.1", "1", "10", "100"], fontsize=10)
    ax.set_ylabel("Odds Ratio (log10 scale)", fontsize=10.6)
    ax.set_xlabel("")
    ax.set_xticks([e["x"] for e in entries])
    ax.set_xticklabels([e["cat"] for e in entries], rotation=45, ha="right", fontsize=9.0)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.set_title("Feature-Wise Enrichment Across Pairwise Comparisons", fontsize=12)


def draw_fisher_heatmap(ax: plt.Axes, report: Dict) -> None:
    n_rows = len(CAT_LABELS)
    n_cols = len(FOREST_DEFS)
    z = np.full((n_rows, n_cols), np.nan, dtype=float)
    stars: List[List[str]] = [["" for _ in range(n_cols)] for _ in range(n_rows)]

    for j, (comp, _, _) in enumerate(FOREST_DEFS):
        rows = _ordered_fisher_rows(report, comp)
        label_to_row = {r["cat"]: r for r in rows}
        for i, label in enumerate(CAT_LABELS):
            if label not in label_to_row:
                continue
            item = label_to_row[label]
            z[i, j] = _bounded_log10_odds(item)
            stars[i][j] = "" if str(item["sig"]) == "ns" else str(item["sig"])

    im = ax.imshow(z, cmap="RdBu_r", aspect="auto", vmin=-2.2, vmax=2.2)
    ax.set_xticks(range(n_cols))
    ax.set_xticklabels([short for _, short, _ in FOREST_DEFS], fontsize=8.2)
    ax.set_yticks(range(n_rows))
    ax.set_yticklabels(CAT_LABELS, fontsize=8.2)
    ax.set_title("Feature Enrichment Matrix (log10 OR + significance)", fontsize=10)

    # Light cell separators improve scanability for print.
    ax.set_xticks(np.arange(-0.5, n_cols, 1), minor=True)
    ax.set_yticks(np.arange(-0.5, n_rows, 1), minor=True)
    ax.grid(which="minor", color="white", linewidth=0.7, alpha=0.85)
    ax.tick_params(which="minor", bottom=False, left=False)

    for i in range(n_rows):
        for j in range(n_cols):
            if np.isnan(z[i, j]):
                continue
            s = stars[i][j]
            if not s:
                continue
            text_color = "white" if abs(z[i, j]) > 1.0 else "black"
            ax.text(j, i, s, ha="center", va="center", fontsize=8.2, color=text_color, fontweight="bold")

    cb = ax.figure.colorbar(im, ax=ax, fraction=0.03, pad=0.02)
    cb.set_label("log10(Odds Ratio)", fontsize=8.5)
    cb.ax.tick_params(labelsize=8)


def draw_fig13(ax: plt.Axes, data: Dict[str, int], title: str, palette: Dict[str, str]) -> None:
    labels = list(data.keys())
    values = [int(data[k]) for k in labels]
    if not values or sum(values) == 0:
        ax.text(0.5, 0.5, f"No data for {title}", ha="center", va="center")
        ax.axis("off")
        return

    colors = [palette.get(label, "#888888") for label in labels]
    wedges, _, autotexts = ax.pie(
        values,
        colors=colors,
        autopct="%1.1f%%",
        pctdistance=0.78,
        startangle=90,
        wedgeprops=dict(width=0.55, edgecolor="white", linewidth=1.2),
        textprops=dict(fontsize=8),
    )
    for txt in autotexts:
        txt.set_fontsize(8.5)
        txt.set_fontweight("medium")

    legend_labels = [f"{l} (n={v})" for l, v in zip(labels, values)]
    ax.legend(
        wedges,
        legend_labels,
        fontsize=9.2,
        loc="upper center",
        bbox_to_anchor=(0.5, 0.04),
        ncol=max(1, len(legend_labels)),
        borderaxespad=0.05,
        handletextpad=0.4,
        columnspacing=0.8,
        framealpha=0.9,
    )
    ax.set_title(title, fontsize=10)


def infer_base_generators(report: Dict) -> List[str]:
    gen_struct_keys = list(report.get("gen_struct", {}).keys())
    inferred = [k for k in gen_struct_keys if "_" not in k and "-" not in k]
    if not inferred:
        combo_keys = list(report.get("gen_combo_top100", {}).keys())
        token_set = set()
        for key in combo_keys:
            token_set.update(t for t in re.split(r"[^a-zA-Z0-9]+", key) if t)
        inferred = sorted(token_set)

    preferred = [k for k in ["gmdldr", "reinvent", "transmol"] if k in inferred]
    extras = sorted([k for k in inferred if k not in preferred])
    return preferred + extras


def expand_combo_counts_to_generators(
    combo_counts: Dict[str, int], generators: List[str]
) -> Dict[str, int]:
    expanded = {g: 0 for g in generators}
    for combo_label, count in combo_counts.items():
        tokens = set(t for t in re.split(r"[^a-zA-Z0-9]+", combo_label) if t)
        for g in generators:
            if g in tokens:
                expanded[g] += int(count)
    return {k: v for k, v in expanded.items() if v > 0}


