def _ordered_fisher_rows(report: Dict, comp: str) -> List[Dict]:
    fisher = report["fisher"]
    rows = [f for f in fisher if f["comp"] == comp]
    if not rows:
        return []
    label_to_row = {r["cat"]: r for r in rows}
    return [label_to_row[label] for label in CAT_LABELS if label in label_to_row]


def benjamini_hochberg(p_values: List[float]) -> List[float]:
    """Benjamini-Hochberg FDR adjustment."""
    m = len(p_values)
    if m == 0:
        return []
    p = np.asarray(p_values, dtype=float)
    order = np.argsort(p)
    p_sorted = p[order]

    q_sorted = np.empty(m, dtype=float)
    prev = 1.0
    for i in range(m - 1, -1, -1):
        rank = i + 1
        q_i = (p_sorted[i] * m) / rank
        prev = min(prev, q_i)
        q_sorted[i] = prev
    q_sorted = np.clip(q_sorted, 0.0, 1.0)

    q = np.empty(m, dtype=float)
    q[order] = q_sorted
    return q.tolist()


def add_fdr_adjusted_significance(report: Dict) -> Dict:
    """Apply BH-FDR to panel-B tests and update sig stars from adjusted q-values."""
    report_adj = json.loads(json.dumps(report))
    fisher_rows = report_adj.get("fisher", [])
    panel_b_comps = {comp for comp, _, _ in FOREST_DEFS}

    idxs = [i for i, row in enumerate(fisher_rows) if row.get("comp") in panel_b_comps]
    pvals = [float(fisher_rows[i]["p"]) for i in idxs]
    qvals = benjamini_hochberg(pvals)

    for i, q in zip(idxs, qvals):
        fisher_rows[i]["q"] = float(q)
        fisher_rows[i]["sig"] = (
            "***" if q < 0.001 else "**" if q < 0.01 else "*" if q < 0.05 else "ns"
        )
    return report_adj


def _bounded_log10_odds(item: Dict, max_abs: float = 2.2) -> float:
    odds = float(item["odds"])
    if np.isinf(odds):
        return max_abs if "↑" in str(item.get("dir", "")) else -max_abs
    x = np.log10(max(odds, 0.01))
    return float(np.clip(x, -max_abs, max_abs))


