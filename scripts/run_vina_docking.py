# VALIDATION
# ===================================================================
def validate_complex(complex_pdb: Path) -> dict:
    """Validate the docked complex: chain IDs, ligand in pocket, contacts."""
    info = {"valid": True, "warnings": [], "lig_atoms": 0,
            "min_contact": None, "pocket_dist": None}

    text = complex_pdb.read_text()
    prot_xyz, lig_xyz = [], []
    pocket_xyz = []

    for line in text.splitlines():
        if len(line) < 54:
            continue
        try:
            x = float(line[30:38])
            y = float(line[38:46])
            z = float(line[46:54])
        except (ValueError, IndexError):
            continue

        if line.startswith("ATOM") and line[21] == "A":
            prot_xyz.append([x, y, z])
            ri = int(line[22:26].strip())
            if ri in VDR_LBD_BINDING:
                pocket_xyz.append([x, y, z])
        elif line.startswith("HETATM") and line[21] == "B":
            lig_xyz.append([x, y, z])

    info["lig_atoms"] = len(lig_xyz)

    if not lig_xyz:
        info["valid"] = False
        info["warnings"].append("No ligand atoms")
        return info

    lig_cen = np.mean(lig_xyz, axis=0)

    if pocket_xyz:
        pocket_cen = np.mean(pocket_xyz, axis=0)
        pocket_dist = float(np.linalg.norm(lig_cen - pocket_cen))
        info["pocket_dist"] = pocket_dist
        if pocket_dist > 20.0:
            info["warnings"].append(f"Ligand {pocket_dist:.1f}A from pocket")

    if prot_xyz:
        prot_arr = np.array(prot_xyz)
        lig_arr = np.array(lig_xyz)
        # Minimum distance between any ligand atom and any protein atom
        min_d = float(np.min(
            np.linalg.norm(prot_arr[None, :, :] - lig_arr[:, None, :],
                           axis=2)))
        info["min_contact"] = min_d
        if min_d > 10.0:
            info["warnings"].append(f"Min contact {min_d:.1f}A (too far)")

    if info["warnings"]:
        info["valid"] = False
    return info


