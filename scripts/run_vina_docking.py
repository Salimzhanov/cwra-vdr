def run_vina(
    ligand_pdbqt: Path,
    out_dir: Path,
    name: str,
    *,
    receptor: Path = RECEPTOR_PDBQT,
    flex_receptor: Path | None = None,
    exhaustiveness: int = 64,
    num_modes: int = 9,
    energy_range: int = 4,
    seed: int = 42,
    timeout: int = 600,
) -> tuple[bool, str, float, float | None]:
    """Run Vina docking. Returns (success, msg, elapsed, best_score)."""
    out_pdbqt = out_dir / f"{name}_out.pdbqt"
    log_file = out_dir / f"{name}_log.txt"

    cmd = [
        VINA_EXE,
        "--receptor", str(receptor),
        "--ligand", str(ligand_pdbqt),
        *(["--flex", str(flex_receptor)] if flex_receptor else []),
        "--center_x", f"{BOX_CENTER[0]:.3f}",
        "--center_y", f"{BOX_CENTER[1]:.3f}",
        "--center_z", f"{BOX_CENTER[2]:.3f}",
        "--size_x", f"{BOX_SIZE[0]:.1f}",
        "--size_y", f"{BOX_SIZE[1]:.1f}",
        "--size_z", f"{BOX_SIZE[2]:.1f}",
        "--exhaustiveness", str(exhaustiveness),
        "--num_modes", str(num_modes),
        "--energy_range", str(energy_range),
        "--seed", str(seed),
        "--out", str(out_pdbqt),
        "--log", str(log_file),
    ]

    t0 = time.time()
    try:
        result = subprocess.run(
            cmd, capture_output=True, text=True,
            timeout=timeout, cwd=str(ROOT),
        )
        elapsed = time.time() - t0

        if result.returncode != 0:
            err = (result.stderr or result.stdout or "unknown")[-400:]
            return False, f"exit {result.returncode}: {err}", elapsed, None

        best_score = parse_vina_log(log_file)
        return True, f"OK ({elapsed:.0f}s)", elapsed, best_score

    except subprocess.TimeoutExpired:
        return False, f"timeout after {timeout}s", time.time() - t0, None
    except Exception as exc:
        return False, str(exc), time.time() - t0, None


