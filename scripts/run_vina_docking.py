# PHARMACOPHORE VALIDATION (Glide XP-inspired)
# ===================================================================
def validate_pharmacophore(complex_pdb: Path) -> dict:
    """Check docked pose for key VDR H-bond interactions.

    1DB1 crystal H-bond network (Rochel et al., Mol Cell, 2000):
    25-OH anchors:  His305-NE2 (2.81 Å), His397-NE2 (2.82 Å)
    1α-OH anchors:  Arg274-NH1 (2.86 Å), Ser237-OG (2.78 Å)
    3β-OH anchors:  Tyr143-OH  (2.83 Å), Ser278-OG (2.91 Å)
    We measure distances from ligand polar atoms (O, N, S) to these
    six pharmacophoric anchor atoms.

    Returns dict: n_hbonds (0-6), pharmacophore_score, per-residue details.
    """
    pharm: dict = {"n_hbonds": 0, "pharmacophore_score": 0.0, "residues": {}}

    text = complex_pdb.read_text()
    key_xyz: dict[tuple[int, str], np.ndarray] = {}
    lig_polar: list[np.ndarray] = []

    for line in text.splitlines():
        if len(line) < 54:
            continue
        try:
            x = float(line[30:38])
            y = float(line[38:46])
            z = float(line[46:54])
        except (ValueError, IndexError):
            continue

        if line.startswith("ATOM") and line[21] == "A":
            try:
                rn = int(line[22:26].strip())
            except ValueError:
                continue
            aname = line[12:16].strip()
            if rn in PHARMACOPHORE:
                key_xyz[(rn, aname)] = np.array([x, y, z])
        elif line.startswith("HETATM") and line[21] == "B":
            elem = line[76:78].strip() if len(line) >= 78 else ""
            if not elem:
                elem = line[12:16].strip()[0] if line[12:16].strip() else "C"
            # Handle AutoDock atom types: OA->O, NA->N, SA->S, HD->H
            if len(elem) > 1:
                elem = elem[0]
            if elem in ("O", "N", "S"):
                lig_polar.append(np.array([x, y, z]))

    if not lig_polar or not key_xyz:
        return pharm

    for rn, info in PHARMACOPHORE.items():
        best_d = float("inf")
        for aname in info["atoms"]:
            if (rn, aname) not in key_xyz:
                continue
            for lp in lig_polar:
                d = float(np.linalg.norm(key_xyz[(rn, aname)] - lp))
                if d < best_d:
                    best_d = d

        satisfied = best_d <= HBOND_DIST_CUTOFF
        pharm["residues"][info["name"]] = {
            "dist": round(best_d, 2) if best_d < 999 else None,
            "satisfied": satisfied,
        }
        if satisfied:
            pharm["n_hbonds"] += 1
            pharm["pharmacophore_score"] += max(
                0, (HBOND_DIST_CUTOFF - best_d) / HBOND_DIST_CUTOFF)

    pharm["pharmacophore_score"] = round(pharm["pharmacophore_score"], 3)
    return pharm


# ===================================================================
# MAIN
# ===================================================================
def main():
    parser = argparse.ArgumentParser(
        description="AutoDock Vina docking into VDR LBD (prepared receptor)")
    parser.add_argument("--manifest", default=str(MANIFEST_PATH))
    parser.add_argument("--output", default=str(OUTPUT_DIR))
    parser.add_argument("--exhaustiveness", type=int, default=64,
                        help="Search thoroughness (8=fast, 32=good, 64=best)")
    parser.add_argument("--num-modes", type=int, default=9,
                        help="Max binding modes to generate")
    parser.add_argument("--energy-range", type=int, default=4,
                        help="Max energy diff from best mode (kcal/mol)")
    parser.add_argument("--seed", type=int, default=42,
                        help="Random seed for reproducibility")
    parser.add_argument("--timeout", type=int, default=600)
    parser.add_argument("--flexible", action="store_true", default=True,
                        help="Use flexible receptor residues (6 pharmacophore "
                             "anchors: Tyr143,Ser237,Arg274,Ser278,His305,His397)")
    parser.add_argument("--no-flexible", dest="flexible", action="store_false",
                        help="Use rigid receptor (no flexible residues)")
    parser.add_argument("--multi-state", action="store_true", default=False,
                        help="Glide-informed: dock tautomer x conformer variants")
    parser.add_argument("--max-tautomers", type=int, default=3,
                        help="Max tautomers per compound (multi-state)")
    parser.add_argument("--max-conformers", type=int, default=3,
                        help="Max conformers per tautomer (multi-state)")
    parser.add_argument("--start", type=int, default=0)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--skip-existing", action="store_true", default=True)
    parser.add_argument("--recompute-pharmacophore", action="store_true",
                        default=False,
                        help="Rebuild complex PDBs and recompute pharmacophore")
    args = parser.parse_args()

    # ── Pharmacophore post-processing mode ──────────────────────────
    if args.recompute_pharmacophore:
        out = Path(args.output)
        progress_csv = out / "progress.csv"
        complex_dir = out / "complex_pdbs"
        docked_dir = out / "docked_pdbqt"
        if not progress_csv.exists():
            print("ERROR: No progress.csv found"); sys.exit(1)
        df_prog = pd.read_csv(progress_csv)

        # Step 1: Rebuild complex PDBs from docked PDBQTs
        rebuilt = 0
        for idx, row in df_prog.iterrows():
            name = row["name"]
            # Try both naming conventions
            docked_file = docked_dir / f"{name}_out.pdbqt"
            if not docked_file.exists():
                docked_file = docked_dir / f"{name}_docked.pdbqt"
            cpdb_path = complex_dir / f"{name}_complex.pdb"
            if docked_file.exists():
                result = create_complex_pdb(RECEPTOR_PDB, docked_file, cpdb_path)
                if result:
                    rebuilt += 1
        print(f"Rebuilt {rebuilt} complex PDBs with corrected elements")

        # Step 2: Recompute pharmacophore on rebuilt complexes
        print(f"Recomputing pharmacophore for {len(df_prog)} compounds...")
        updated = 0
        for idx, row in df_prog.iterrows():
            cpdb_path = complex_dir / f"{row['name']}_complex.pdb"
            if not cpdb_path.exists():
                continue
            pharm = validate_pharmacophore(cpdb_path)
            df_prog.at[idx, "n_hbonds"] = pharm.get("n_hbonds", 0)
            df_prog.at[idx, "pharmacophore_score"] = pharm.get(
                "pharmacophore_score", 0.0)
            for rn_key, rn_info in PHARMACOPHORE.items():
                col = f"pharm_{rn_info['name']}"
                dist = pharm.get("residues", {}).get(
                    rn_info["name"], {}).get("dist")
                df_prog.at[idx, col] = dist
            updated += 1
        df_prog.to_csv(progress_csv, index=False)
        print(f"Updated {updated} rows in {progress_csv}")
        if "n_hbonds" in df_prog.columns:
            hb = df_prog["n_hbonds"].dropna()
            print(f"H-bonds: mean={hb.mean():.1f}  "
                  f"0-hb={int((hb==0).sum())}  1-hb={int((hb==1).sum())}  "
                  f"2-hb={int((hb==2).sum())}  3-hb={int((hb==3).sum())}  "
                  f"4-hb={int((hb==4).sum())}")
        sys.exit(0)

    # Validate prerequisites
    if not Path(VINA_EXE).exists():
        print(f"ERROR: Vina not found at {VINA_EXE}")
        sys.exit(1)
    if not RECEPTOR_PDBQT.exists():
        print(f"ERROR: Receptor PDBQT not found at {RECEPTOR_PDBQT}")
        print("  Generate with: python scripts/prepare_1db1_receptor.py")
        sys.exit(1)
    if not RECEPTOR_PDB.exists():
        print(f"ERROR: Receptor PDB not found at {RECEPTOR_PDB}")
        sys.exit(1)

    # Flexible receptor setup
    flex_path = None
    rec_path = RECEPTOR_PDBQT
    if args.flexible:
        if not RECEPTOR_RIGID.exists() or not RECEPTOR_FLEX.exists():
            print("ERROR: Rigid/Flex PDBQT not found. Run prepare_1db1_receptor.py first.")
            print(f"  Expected: {RECEPTOR_RIGID}")
            print(f"  Expected: {RECEPTOR_FLEX}")
            sys.exit(1)
        rec_path = RECEPTOR_RIGID
        flex_path = RECEPTOR_FLEX
        print(f"  Flexible docking: {RECEPTOR_FLEX.name}"
              f" (6 pharmacophore anchor residues)")

    df = pd.read_csv(args.manifest)
    if args.start > 0:
        df = df.iloc[args.start:]
    if args.limit is not None:
        df = df.iloc[:args.limit]

    out = Path(args.output)
    out.mkdir(parents=True, exist_ok=True)
    lig_dir = out / "ligand_pdbqt"
    lig_dir.mkdir(exist_ok=True)
    docked_dir = out / "docked_pdbqt"
    docked_dir.mkdir(exist_ok=True)
    logs_dir = out / "logs"
    logs_dir.mkdir(exist_ok=True)
    complex_dir = out / "complex_pdbs"
    complex_dir.mkdir(exist_ok=True)
    progress_csv = out / "progress.csv"

    # Resume support
    done_names = set()
    if progress_csv.exists():
        prev = pd.read_csv(progress_csv)
        done_names = set(prev[prev["status"] == "success"]["name"])

    total = len(df)
    to_do = sum(
        1 for _, row in df.iterrows()
        if f"{row['source']}_{row['csv_row']}" not in done_names
    )

    print("=" * 70)
    print("AUTODOCK VINA DOCKING -- VDR LBD (1DB1 crystal, 250 res)")
    print("=" * 70)
    print(f"Receptor:  {RECEPTOR_PDBQT.name} (res 120-423, 1DB1 crystal, pH 7.4)")
    print(f"Pocket:    {VDR_LBD_BINDING} (LBD numbering)")
    print(f"Box center: ({BOX_CENTER[0]:.2f}, {BOX_CENTER[1]:.2f},"
          f" {BOX_CENTER[2]:.2f})")
    print(f"Box size:   ({BOX_SIZE[0]:.0f}, {BOX_SIZE[1]:.0f},"
          f" {BOX_SIZE[2]:.0f}) A")
    flex_tag = "  FLEX=6-res" if args.flexible else "  RIGID"
    ms_tag = (f"  multi-state={args.max_tautomers}t x {args.max_conformers}c"
              if args.multi_state else "  single-state")
    print(f"Settings:  exhaust={args.exhaustiveness}  modes={args.num_modes}"
          f"  E-range={args.energy_range}  seed={args.seed}{flex_tag}{ms_tag}")
    print(f"Total:     {total} compounds | Done: {len(done_names)}"
          f" | Remaining: {to_do}")
    print("=" * 70)

    rows: list[dict] = []
    n_ok, n_fail, n_skip = 0, 0, 0
    times: list[float] = []
    t_start = time.time()

    for i, (_, row) in enumerate(df.iterrows()):
        src = row["source"]
        csvr = row["csv_row"]
        name = f"{src}_{csvr}"
        tag = f"[{i+1}/{total}]"

        if args.skip_existing and name in done_names:
            n_skip += 1
            continue

        smiles = row["smiles"]
        smi_short = (smiles[:55] + "...") if len(smiles) > 55 else smiles
        print(f"\n{tag} {name}  {smi_short}")

        # ── Step 1 + 2: Ligand prep → Vina docking ─────────────
        if args.multi_state:
            # Glide-informed: enumerate tautomers x conformers
            variants = smiles_to_pdbqt_variants(
                smiles, name, lig_dir,
                max_tautomers=args.max_tautomers,
                max_conformers=args.max_conformers,
            )
            if not variants:
                print(f"  FAIL (prep): no valid variants")
                rows.append({
                    "name": name, "source": src, "csv_row": csvr,
                    "smiles": smiles, "status": "failed_prep",
                    "error": "no variants",
                })
                n_fail += 1
                continue

            print(f"  Multi-state: {len(variants)} variants")
            best_score = None
            best_variant = None
            total_elapsed = 0.0

            for vpdbqt, vlabel in variants:
                vok, vmsg, vel, vsc = run_vina(
                    vpdbqt, docked_dir, vlabel,
                    receptor=rec_path,
                    flex_receptor=flex_path,
                    exhaustiveness=args.exhaustiveness,
                    num_modes=args.num_modes,
                    energy_range=args.energy_range,
                    seed=args.seed,
                    timeout=args.timeout,
                )
                total_elapsed += vel or 0.0
                vlog = docked_dir / f"{vlabel}_log.txt"
                if vlog.exists():
                    shutil.move(str(vlog),
                                str(logs_dir / f"{vlabel}_log.txt"))
                if vok and vsc is not None:
                    if best_score is None or vsc < best_score:
                        best_score = vsc
                        best_variant = vlabel

            if best_score is None:
                print(f"  FAIL (dock): all {len(variants)} variants failed")
                rows.append({
                    "name": name, "source": src, "csv_row": csvr,
                    "smiles": smiles, "status": "failed_dock",
                    "error": "all variants failed",
                    "elapsed": total_elapsed,
                })
                n_fail += 1
                continue

            ok, elapsed, msg = True, total_elapsed, f"best={best_variant}"
            # Copy best variant outputs to standard names
            bv_out = docked_dir / f"{best_variant}_out.pdbqt"
            std_out = docked_dir / f"{name}_out.pdbqt"
            if bv_out.exists():
                shutil.copy2(str(bv_out), str(std_out))
            bv_log = logs_dir / f"{best_variant}_log.txt"
            std_log = logs_dir / f"{name}_log.txt"
            if bv_log.exists():
                shutil.copy2(str(bv_log), str(std_log))

        else:
            # Original single-state pipeline
            prep_ok, prep_msg, lig_pdbqt = smiles_to_pdbqt(
                smiles, name, lig_dir)
            if not prep_ok:
                print(f"  FAIL (prep): {prep_msg}")
                rows.append({
                    "name": name, "source": src, "csv_row": csvr,
                    "smiles": smiles, "status": "failed_prep",
                    "error": prep_msg,
                })
                n_fail += 1
                continue

            ok, msg, elapsed, best_score = run_vina(
                lig_pdbqt, docked_dir, name,
                receptor=rec_path,
                flex_receptor=flex_path,
                exhaustiveness=args.exhaustiveness,
                num_modes=args.num_modes,
                energy_range=args.energy_range,
                seed=args.seed,
                timeout=args.timeout,
            )
            dock_log = docked_dir / f"{name}_log.txt"
            if dock_log.exists():
                shutil.move(str(dock_log),
                            str(logs_dir / f"{name}_log.txt"))

        if not ok:
            print(f"  FAIL (dock): {msg}")
            rows.append({
                "name": name, "source": src, "csv_row": csvr,
                "smiles": smiles, "status": "failed_dock", "error": msg,
                "elapsed": elapsed,
            })
            n_fail += 1
        else:
            # Parse all scores
            log_path = logs_dir / f"{name}_log.txt"
            all_scores = parse_all_scores(log_path)
            n_modes = len(all_scores)

            # Step 3: Create complex PDB (receptor + best pose)
            docked_file = docked_dir / f"{name}_out.pdbqt"
            complex_pdb = complex_dir / f"{name}_complex.pdb"
            cpdb = create_complex_pdb(RECEPTOR_PDB, docked_file, complex_pdb)

            # Step 4: Validate complex
            val = {}
            if cpdb:
                val = validate_complex(cpdb)

            # Step 5: Pharmacophore check (Glide XP-inspired)
            pharm = {}
            if cpdb:
                pharm = validate_pharmacophore(cpdb)
            n_hb = pharm.get("n_hbonds", 0)
            pscore = pharm.get("pharmacophore_score", 0.0)

            cpdb_str = str(cpdb) if cpdb else None
            contact_s = (f"contact={val['min_contact']:.1f}A"
                         if val.get("min_contact") else "")
            pocket_s = (f"pocket={val['pocket_dist']:.1f}A"
                        if val.get("pocket_dist") else "")

            score_s = f"{best_score:.2f}" if best_score is not None else "?"
            status_tag = "OK" if cpdb else "NO-PDB"
            hb_s = f"Hbond={n_hb}/6"
            print(f"  {status_tag}  score={score_s} kcal/mol  modes={n_modes}"
                  f"  {contact_s}  {pocket_s}  {hb_s}  ({elapsed:.0f}s)")

            if val.get("warnings"):
                for w in val["warnings"]:
                    print(f"    WARNING: {w}")

            # Per-residue pharmacophore details
            for rname, rdata in pharm.get("residues", {}).items():
                d_str = f"{rdata['dist']:.1f}A" if rdata.get('dist') else "N/A"
                tag_s = "OK" if rdata.get("satisfied") else "--"
                print(f"    {tag_s} {rname}: {d_str}")

            times.append(elapsed)
            n_ok += 1

            rows.append({
                "name": name, "source": src, "csv_row": csvr,
                "smiles": smiles, "status": "success",
                "vina_score": best_score,
                "n_modes": n_modes,
                "all_scores": ";".join(f"{s:.2f}" for s in all_scores),
                "complex_pdb": cpdb_str,
                "min_contact": val.get("min_contact"),
                "pocket_dist": val.get("pocket_dist"),
                "lig_atoms": val.get("lig_atoms"),
                "n_hbonds": n_hb,
                "pharmacophore_score": pscore,
                "pharm_His305": pharm.get("residues", {}).get("His305", {}).get("dist"),
                "pharm_His397": pharm.get("residues", {}).get("His397", {}).get("dist"),
                "pharm_Arg274": pharm.get("residues", {}).get("Arg274", {}).get("dist"),
                "pharm_Ser237": pharm.get("residues", {}).get("Ser237", {}).get("dist"),
                "pharm_Tyr143": pharm.get("residues", {}).get("Tyr143", {}).get("dist"),
                "pharm_Ser278": pharm.get("residues", {}).get("Ser278", {}).get("dist"),
                "elapsed": elapsed,
            })

        # Save progress + ETA
        batch_df = pd.DataFrame(rows)
        if progress_csv.exists():
            prev = pd.read_csv(progress_csv)
            prev = prev[~prev["name"].isin(batch_df["name"])]
            batch_df = pd.concat([prev, batch_df], ignore_index=True)
        batch_df.to_csv(progress_csv, index=False)

        done_so_far = n_ok + n_fail
        if times:
            avg_t = np.mean(times)
            remaining = to_do - done_so_far
            eta_min = remaining * avg_t / 60
            print(f"  ETA: {eta_min:.0f} min ({eta_min/60:.1f}h) | "
                  f"avg={avg_t:.0f}s/cpd | done={done_so_far}/{to_do}")

    # Final summary
    wall = time.time() - t_start
    print("\n" + "=" * 70)
    print("SUMMARY")
    print("=" * 70)
    print(f"  Success: {n_ok}  |  Failed: {n_fail}  |  Skipped: {n_skip}")
    print(f"  Wall time: {wall/60:.1f} min")
    if times:
        print(f"  Avg time:  {np.mean(times):.1f}s/compound")
    print(f"  Output:    {out}")
    print(f"  Complexes: {complex_dir}")
    print(f"  Progress:  {progress_csv}")

    if progress_csv.exists():
        final = pd.read_csv(progress_csv)
        if "vina_score" in final.columns:
            vals = final["vina_score"].dropna()
            if len(vals) > 0:
                print(f"\n  Vina scores: mean={vals.mean():.2f}"
                      f"  best={vals.min():.2f}  worst={vals.max():.2f}")
                top10 = final.nsmallest(10, "vina_score")[
                    ["name", "vina_score"]]
                print("\n  Top 10 by Vina score:")
                for _, r in top10.iterrows():
                    print(f"    {r['name']:15s}"
                          f"  {r['vina_score']:.2f} kcal/mol")

        if "min_contact" in final.columns:
            mc = final["min_contact"].dropna()
            if len(mc) > 0:
                print(f"\n  Min contact: mean={mc.mean():.1f}A"
                      f"  min={mc.min():.1f}A  max={mc.max():.1f}A")

        if "n_hbonds" in final.columns:
            hb = final["n_hbonds"].dropna()
            if len(hb) > 0:
                print(f"\n  Pharmacophore H-bonds (of 6 VDR anchors):")
                print(f"    mean={hb.mean():.1f}  "
                      f"0-hb={int((hb==0).sum())}  "
                      f"1-hb={int((hb==1).sum())}  "
                      f"2-hb={int((hb==2).sum())}  "
                      f"3-hb={int((hb==3).sum())}  "
                      f"4-hb={int((hb==4).sum())}  "
                      f"5-hb={int((hb==5).sum())}  "
                      f"6-hb={int((hb==6).sum())}")

        n_complex = final["complex_pdb"].notna().sum() if "complex_pdb" in final.columns else 0
        print(f"  Complex PDBs: {n_complex}/{len(final)}")


if __name__ == "__main__":
    main()
