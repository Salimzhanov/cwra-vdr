# ===================================================================
def parse_vina_log(log_path: Path) -> float | None:
    """Parse best binding affinity (mode 1) from Vina log."""
    if not log_path.exists():
        return None
    try:
        text = log_path.read_text()
        in_results = False
        for line in text.splitlines():
            line = line.strip()
            if "-----+" in line:
                in_results = True
                continue
            if in_results and line and line[0].isdigit():
                parts = line.split()
                if len(parts) >= 2:
                    return float(parts[1])
    except Exception:
        pass
    return None


def parse_all_scores(log_path: Path) -> list[float]:
    """Parse all binding mode scores from Vina log."""
    scores = []
    if not log_path.exists():
        return scores
    try:
        text = log_path.read_text()
        in_results = False
        for line in text.splitlines():
            line = line.strip()
            if "-----+" in line:
                in_results = True
                continue
            if in_results and line and line[0].isdigit():
                parts = line.split()
                if len(parts) >= 2:
                    try:
                        scores.append(float(parts[1]))
                    except ValueError:
                        break
            elif in_results and not line:
                break
    except Exception:
        pass
    return scores


