# GLIDE-INFORMED MULTI-STATE LIGAND PREPARATION
# ===================================================================
def enumerate_tautomers(mol, max_n: int = 5):
    """Enumerate dominant tautomers (approximates Glide LigPrep / Epik).

    RDKit's TautomerEnumerator applies standard chemical transform rules
    for keto-enol, amide-imidic acid, 1,3-shifts, etc.
    Returns canonical tautomer first, then alternatives.
    """
    try:
        enumerator = rdMolStandardize.TautomerEnumerator()
        canonical = enumerator.Canonicalize(mol)
        seen = {Chem.MolToSmiles(canonical)}
        tautomers = [canonical]
        for taut in enumerator.Enumerate(mol):
            if len(tautomers) >= max_n:
                break
            smi = Chem.MolToSmiles(taut)
            if smi not in seen:
                seen.add(smi)
                tautomers.append(taut)
        return tautomers
    except Exception:
        return [mol]


def smiles_to_pdbqt_variants(
    smiles: str,
    name: str,
    out_dir: Path,
    max_tautomers: int = 3,
    max_conformers: int = 3,
) -> list[tuple[Path, str]]:
    """Generate multiple ligand variants following Glide LigPrep concepts.

    For each tautomer x conformer combination:
      1. Enumerate dominant tautomers (RDKit MolStandardize)
      2. Generate diverse 3-D conformers with different ETKDG seeds
      3. MMFF-optimize each conformer
      4. Convert to PDBQT via meeko

    Returns list of (pdbqt_path, variant_label) tuples.
    The best Vina score across all variants is kept.
    """
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return []

    tautomers = enumerate_tautomers(mol, max_tautomers)
    variants: list[tuple[Path, str]] = []

    for t_idx, taut in enumerate(tautomers):
        for c_idx in range(max_conformers):
            label = f"{name}_t{t_idx}_c{c_idx}"
            taut_h = Chem.AddHs(Chem.Mol(taut))   # fresh copy + H
            seed = 42 + c_idx * 17

            params = AllChem.ETKDGv3()
            params.randomSeed = seed
            status = AllChem.EmbedMolecule(taut_h, params)
            if status != 0:
                params.useRandomCoords = True
                params.maxIterations = 2000
                status = AllChem.EmbedMolecule(taut_h, params)
                if status != 0:
                    continue

            try:
                res = AllChem.MMFFOptimizeMolecule(taut_h, maxIters=1000)
                if res != 0:
                    AllChem.UFFOptimizeMolecule(taut_h, maxIters=1000)
            except Exception:
                try:
                    AllChem.UFFOptimizeMolecule(taut_h, maxIters=1000)
                except Exception:
                    pass

            try:
                prep = MoleculePreparation()
                mol_setups = prep.prepare(taut_h)
                pdbqt_str, _, _ = PDBQTWriterLegacy.write_string(
                    mol_setups[0])
            except Exception:
                continue

            if not pdbqt_str or len(pdbqt_str.strip()) < 50:
                continue

            p = out_dir / f"{label}.pdbqt"
            p.write_text(pdbqt_str, encoding="utf-8")
            variants.append((p, label))

    return variants


