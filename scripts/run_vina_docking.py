def smiles_to_pdbqt(smiles: str, name: str, out_dir: Path,
                    max_attempts: int = 5) -> tuple[bool, str, Path | None]:
    """Convert SMILES to PDBQT via RDKit 3D + meeko.

    Returns (success, message, pdbqt_path).
    """
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return False, "RDKit parse failed", None

    mol = Chem.AddHs(mol)

    # Try multiple random seeds for 3D embedding
    params = AllChem.ETKDGv3()
    embedded = False
    for attempt in range(max_attempts):
        params.randomSeed = 42 + attempt
        status = AllChem.EmbedMolecule(mol, params)
        if status == 0:
            embedded = True
            break

    if not embedded:
        # Fallback: random coordinates with more iterations
        params.useRandomCoords = True
        params.maxIterations = 2000
        status = AllChem.EmbedMolecule(mol, params)
        if status != 0:
            return False, f"3D embed failed after {max_attempts} attempts", None

    # Force field optimisation (MMFF preferred, UFF fallback)
    try:
        res = AllChem.MMFFOptimizeMolecule(mol, maxIters=1000)
        if res != 0:
            AllChem.UFFOptimizeMolecule(mol, maxIters=1000)
    except Exception:
        try:
            AllChem.UFFOptimizeMolecule(mol, maxIters=1000)
        except Exception:
            pass  # proceed with unoptimised coords

    # Meeko PDBQT conversion
    try:
        prep = MoleculePreparation()
        mol_setups = prep.prepare(mol)
        pdbqt_str, is_ok, err_msg = PDBQTWriterLegacy.write_string(
            mol_setups[0])
    except Exception as e:
        return False, f"meeko error: {e}", None

    if not pdbqt_str or len(pdbqt_str.strip()) < 50:
        return False, "empty PDBQT output", None

    # Fix invalid meeko atom types (CG0, G0, etc.) that crash Vina
    pdbqt_str = _fix_meeko_atom_types(pdbqt_str)

    pdbqt_path = out_dir / f"{name}.pdbqt"
    pdbqt_path.write_text(pdbqt_str, encoding="utf-8")
    return True, "OK", pdbqt_path


def _fix_meeko_atom_types(pdbqt_str: str) -> str:
    """Fix invalid AutoDock atom types produced by meeko.

    Meeko sometimes outputs non-standard types like CG0, G0, CG1 for
    complex ring systems (spiro, bridged, etc.).  Vina only accepts
    standard AD4 types: C, A, N, NA, OA, S, SA, HD, H, F, Cl, Br, I, P.

    Strategy:
      CG0 / CG1 / CG2 -> C   (carbon)
      G0 / G1          -> C   (meeko "glue" pseudo-atom → treat as carbon)
      Lines with just "G" element that start with "ATOM" for G0 pseudo-atoms
      are dropped entirely since they are duplicates.
    """
    fixed_lines = []
    for line in pdbqt_str.splitlines():
        if not (line.startswith("ATOM") or line.startswith("HETATM")):
            fixed_lines.append(line)
            continue

        # Check last field (atom type at col 77+)
        if len(line) >= 78:
            ad_type = line[77:].strip()
        elif len(line) >= 70:
            # try splitting
            parts = line.split()
            ad_type = parts[-1] if parts else ""
        else:
            fixed_lines.append(line)
            continue

        # Fix known bad types
        if ad_type in ("CG0", "CG1", "CG2", "CG3"):
            # Replace atom type with C, fix atom name too
            line = line[:12] + " C  " + line[16:77] + " C "
            fixed_lines.append(line)
        elif ad_type in ("G0", "G1", "G2", "G3"):
            # These are meeko "glue" pseudo-atoms — skip entirely
            # They are duplicate coordinate entries that Vina can't parse
            continue
        else:
            fixed_lines.append(line)

    return "\n".join(fixed_lines)


