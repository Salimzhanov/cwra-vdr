def extract_best_pose_pdbqt(vina_out_pdbqt: Path,
                            strip_flex: bool = True) -> str | None:
    """Extract MODEL 1 (best pose) from multi-model Vina output.

    When strip_flex=True (default), removes BEGIN_RES/END_RES blocks
    (flex-residue atoms from Vina --flex output), returning ligand only.
    This ensures create_complex_pdb() gets only ligand PDBQT lines.
    """
    if not vina_out_pdbqt.exists():
        return None
    lines = vina_out_pdbqt.read_text().splitlines()
    pose_lines = []
    in_model1 = False
    in_flex_res = False
    for line in lines:
        if line.startswith("MODEL"):
            model_num = line.split()[-1]
            if model_num == "1":
                in_model1 = True
            elif in_model1:
                break
        elif in_model1:
            if line.startswith("ENDMDL"):
                break
            # Strip flex-residue blocks from Vina --flex output
            if strip_flex:
                if line.startswith("BEGIN_RES"):
                    in_flex_res = True
                    continue
                if line.startswith("END_RES"):
                    in_flex_res = False
                    continue
                if in_flex_res:
                    continue
                if line.startswith(("ROOT", "ENDROOT", "BRANCH", "ENDBRANCH")):
                    continue
            pose_lines.append(line)
    return "\n".join(pose_lines) if pose_lines else None


# AutoDock atom type -> standard PDB element symbol
_AD_TYPE_TO_ELEMENT: dict[str, str] = {
    "C": "C",  "A": "C",                   # aliphatic / aromatic carbon
    "OA": "O", "OS": "O",                  # oxygen acceptor / sulfate O
    "N": "N",  "NA": "N", "NS": "N",       # nitrogen / acceptor / sulfonamide
    "HD": "H", "H": "H",  "HS": "H",       # hydrogen donor / plain H / S-H
    "S": "S",  "SA": "S",                  # sulfur / sulfur acceptor
    "P": "P",  "F": "F",  "I": "I",
    "Cl": "Cl", "Br": "Br",
    "Fe": "Fe", "Zn": "Zn", "Mg": "Mg", "Ca": "Ca", "Mn": "Mn",
}


def pdbqt_to_pdb_lines(pdbqt_text: str, chain: str = "B",
                       resname: str = "LIG", resnum: int = 1) -> list[str]:
    """Convert PDBQT ATOM lines to standard PDB HETATM lines."""
    pdb_lines = []
    serial = 1
    for line in pdbqt_text.splitlines():
        if not (line.startswith("ATOM") or line.startswith("HETATM")):
            continue
        try:
            x = float(line[30:38])
            y = float(line[38:46])
            z = float(line[46:54])
        except (ValueError, IndexError):
            continue

        atom_name = line[12:16].strip()

        # Read AutoDock atom type from PDBQT column 77-79
        ad_type = ""
        if len(line) >= 78:
            ad_type = line[77:79].strip()
        if not ad_type:
            ad_type = atom_name[0] if atom_name else "C"

        # Convert AutoDock atom type to standard PDB element symbol
        element = _AD_TYPE_TO_ELEMENT.get(ad_type, ad_type[0] if ad_type else "C")

        an = f" {atom_name:<3s}" if len(atom_name) < 4 else atom_name[:4]
        pdb_line = (
            f"HETATM{serial:5d} {an:4s} {resname:3s} {chain}{resnum:4d}    "
            f"{x:8.3f}{y:8.3f}{z:8.3f}  1.00  0.00          {element:>2s}  "
        )
        pdb_lines.append(pdb_line)
        serial += 1
    return pdb_lines


def create_complex_pdb(
    receptor_pdb: Path,
    docked_pdbqt: Path,
    output_pdb: Path,
) -> Path | None:
    """Merge receptor PDB (chain A) + best docked pose (chain B) into complex.

    Produces a properly formatted PDB with:
      - ATOM records for protein (chain A, res 120-423, 1DB1 numbering)
      - TER
      - HETATM records for ligand (chain B, LIG 1)
      - END
    """
    pose_text = extract_best_pose_pdbqt(docked_pdbqt)
    if pose_text is None:
        return None

    lig_lines = pdbqt_to_pdb_lines(pose_text, chain="B")
    if not lig_lines:
        return None

    # Read receptor PDB
    rec_lines = []
    last_serial = 0
    for line in receptor_pdb.read_text().splitlines():
        if line.startswith("ATOM"):
            # Ensure chain A is set
            fixed = line[:21] + "A" + line[22:]
            rec_lines.append(fixed)
            try:
                last_serial = int(line[6:11].strip())
            except ValueError:
                pass

    # TER after last protein atom, referencing last protein residue
    ter_serial = last_serial + 1
    # Find last protein residue name/number for proper TER record
    last_resname = "UNK"
    last_resnum = 0
    for rl in reversed(rec_lines):
        if rl.startswith("ATOM"):
            last_resname = rl[17:20].strip()
            try:
                last_resnum = int(rl[22:26].strip())
            except ValueError:
                pass
            break

    # Renumber ligand serials continuing after TER
    renumbered_lig = []
    for i, ll in enumerate(lig_lines, start=1):
        new_serial = ter_serial + i
        renumbered_lig.append(f"{ll[:6]}{new_serial:5d}{ll[11:]}")

    # Assemble
    out_lines = rec_lines
    out_lines.append(
        f"TER   {ter_serial:5d}      {last_resname:>3s} A{last_resnum:4d}")
    out_lines.extend(renumbered_lig)
    out_lines.append("END")

    output_pdb.write_text("\n".join(out_lines) + "\n", encoding="utf-8")
    return output_pdb


